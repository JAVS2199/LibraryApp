public class Book {
    private String isbn;
    private String title;
    private String description;
    private String author;
    private String genre;

    public Book(String isbn, String title, String description, String author, String genre) {
        this.isbn = isbn;
        this.title = title;
        this.description = description;
        this.author = author;
        this.genre = genre;
    }
}
